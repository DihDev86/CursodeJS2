//Os operadores aritiméticos são: + , -, * , /, % e ** que não funciona em todos os navegadores.

let n1 = 10;
let n2 = 5;

console.log(n1 + n2) //Operação de adição " + "
console.log(n1 - n2) //Operação de subtração " - "
console.log(n1 * n2) //Operação de multiplicação " * "
console.log(n1 / n2) //Operação de divisão " / "
console.log(n1 % n2) //Operação de resto da divisão " % " (muito ultilizado quando queremos recuperar números multiplos de 3.)
//Exemplo: Descubrir se número é par ou impar (Se um número for dividido por 2 e o resto for zero, ele será par, mas se o resto for > 0 ele será impar.)

//Para saber números multiplos de 3, sabemos quando não resta nada na divisão, exemplo: 9 % 3, não resta nada, logo 9 é multiplo de 3.

console.log(2 ** 3) //Operação de exponeciação 
