//Operadores de incremento e decremento.
//Serve para incrementar ou decrementar valores

//Incremento pós fixado (X++)
//Decremento pós fixado (X--)
//Incremento pré fixado (++X)
//Descremento pré fixado (--X)


let i = 0;
i++ // pós decremento
console.log(i)
//Foi incrementado mais 1 ao valor da variável

let i2 = 2;
i2-- // pós decremento (mostra primeiro o valor da variável e depois o valor decrementato)
console.log(i2)
//Foi descrementado menos 1 ao valor da variável

let i3 = 2;
++i3 //Pré decremento
console.log(i3)

let i4 = 2;
--i4 //Pré decremento
console.log(i4)